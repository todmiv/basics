# Variables to describe a student
name = "John Doe"  # string type
age = 20  # integer type
grade = 10  # integer type
is_honors_student = True  # boolean type
favorite_lessons = ['mathematics', 'physics', 'chemistry'] # list type
average_grade = 4.5  # float type

# Print the type of each variable
print(type(name))  # <class 'str'>
print(type(age))  # <class 'int'>
print(type(grade))  # <class 'int'>
print(type(is_honors_student))  # <class 'bool'>
print(type(favorite_lessons))  # <class 'list'>
print(type(average_grade))  # <class 'float'>
